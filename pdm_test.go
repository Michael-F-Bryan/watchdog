package main

import (
	"bytes"
	"testing"
)

func TestSerializePacket(t *testing.T) {
	pkt := NewPdmPacket("Michael_Bryan", "mic")

	shouldBe := []byte{0x48, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x1a, 0x00, 0x00,
		0x00, 0x4d, 0x00, 0x69, 0x00, 0x63, 0x00, 0x68, 0x00, 0x61, 0x00,
		0x65, 0x00, 0x6c, 0x00, 0x5f, 0x00, 0x42, 0x00, 0x72, 0x00, 0x79,
		0x00, 0x61, 0x00, 0x6e, 0x00, 0x01, 0x00, 0x00, 0x00, 06, 0x00, 0x00,
		0x00, 0x6d, 0x00, 0x69, 0x00, 0x63, 0x00, 0x01, 0x00, 0x00, 0x00, 00,
		0x00, 0x00, 0x00, 0x36, 0x00, 0x00, 0x00, 0x32, 0x45, 0x0a, 0x85,
		0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00}

	buffer := bytes.NewBuffer([]byte{})
	err := pkt.Serialize(buffer)
	if err != nil {
		t.Errorf("Unexpected error: %v", err)
	}

	data := buffer.Bytes()

	if len(data) != len(shouldBe) {
		t.Fatalf("Expected %d bytes, got %d", len(shouldBe), len(data))
	}

	for i := range data {
		if data[i] != shouldBe[i] {
			t.Errorf("At byte %d expected %x, got %x", i, shouldBe[i], data[i])
		}
	}
}
